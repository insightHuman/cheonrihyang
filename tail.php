<script>
   $(".js__suga-open").click(function() {
      $(".suga-pop").fadeIn(500);
   });

   $(".js__pop-close").click(function() {
      $(".all-pop").fadeOut(500);
   });

   $(".js__suga-btn").click(function() {
      $(".js__suga-btn").removeClass("on");
      $(this).addClass("on");
      $(".suga-pop .pop-block").removeClass("on");
      var target = $(this).attr("suga-attr");
      $(".suga-pop .pop-block-" + target).addClass("on");

   });
   // $(document).on('click', '.js__op-close-btn', function() {
   //    $('.js__quick-opend-target').toggleClass("opend");
   //    $('.js__quick-opend-target').toggleClass("closed");
   // });

   $(document).on('click', '.js__header-btn', function() {
      $("body").toggleClass("mo__fixed");
      $("html").toggleClass("mo__fixed");
      $(this).toggleClass("opend");
      $(".js__header").toggleClass("openb");
      $(".js__ham-menu").toggleClass("opend");
   });

   // $(document).on('click', '.js__lang-btn', function() {
   //    $("body").toggleClass("mo__fixed");
   //    $("html").toggleClass("mo__fixed");
   //    $(".lang-pop").toggleClass("opend");
   // });

   let lastScrollTop = 0;
   const header = document.querySelector('.js__header');
   const ham = document.querySelector('.js__ham-menu');
   const nav = document.querySelector('.js__nav-target');
   const lang = document.querySelector('.lang-pop');

   window.addEventListener('scroll', () => {
      const currentScroll = window.pageYOffset || document.documentElement.scrollTop;

      if (currentScroll > lastScrollTop) {
         header?.classList.remove('opens', 'openb', 'opend');
         ham?.classList.remove('opend');
         nav?.classList.remove('opend');
         lang?.classList.remove('opend');
      } else {
         if (currentScroll === 0) {
            header?.classList.add('opens');
            header?.classList.remove('opend');
            nav?.classList.remove('opend');
            lang?.classList.remove('opend');
         } else {
            header?.classList.add('opend');
            header?.classList.remove('opens');
            nav?.classList.add('opend');
            lang?.classList.remove('opend');
         }
      }

      lastScrollTop = currentScroll <= 0 ? 0 : currentScroll;
   });

   $(".js__top-btn").click(function() {
      $('html, body').animate({
         scrollTop: 0
      }, 500);
   });

   $(".js__sec_move-btn").click(function() {
      // $(".js__header").removeClass("opend");
      var jinz = $(this).attr("move-attr");
      var targetElement = $(".main__section-wrap-" + jinz);
      var targetOffset = targetElement.offset();

      // var paddingTop = parseFloat(targetElement.find(".main__section-inner").css("padding-top")) || 0;
      var paddingTop = 0;

      $("html, body").animate({
         scrollTop: targetOffset.top - paddingTop
      }, 500);

      $("body").removeClass("opend");
      $("html").removeClass("opend");
      $(".js__ham-menu").removeClass("opend");
      $(".js__header").removeClass("openb");
   });
</script>

